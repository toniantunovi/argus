class TestGeneratedHeaderDetection:
    def test_do_not_edit_header(self, tmp_path):
        f = tmp_path / "gen.py"
        f.write_text("# DO NOT EDIT - auto-generated\ndef foo(): pass\n")
        assert has_generated_header(f)

    def test_auto_generated_header(self, tmp_path):
        f = tmp_path / "gen.go"
        f.write_text("// AUTO-GENERATED by protoc\npackage main\n")
        assert has_generated_header(f)

    def test_code_generated_by(self, tmp_path):
        f = tmp_path / "gen.java"
        f.write_text("// Code generated by swagger-codegen\npublic class Foo {}\n")
        assert has_generated_header(f)

    def test_generated_annotation(self, tmp_path):
        f = tmp_path / "gen.py"
        f.write_text("# @generated\nimport os\n")
        assert has_generated_header(f)

    def test_normal_header_not_detected(self, tmp_path):
        f = tmp_path / "app.py"
        f.write_text('"""Application module."""\nimport os\n')
        assert not has_generated_header(f)

    def test_marker_after_max_lines_not_detected(self, tmp_path):
        f = tmp_path / "late.py"
        lines = ["# line\n"] * 15 + ["# DO NOT EDIT\n"]
        f.write_text("".join(lines))
        assert not has_generated_header(f)


