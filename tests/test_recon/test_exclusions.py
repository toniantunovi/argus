class TestIncludeOverrides:
    def test_override_bypasses_exclusion(self, tmp_path):
        project_root = tmp_path / "project"
        vendor_dir = project_root / "vendor" / "lib"
        vendor_dir.mkdir(parents=True)
        f = vendor_dir / "util.py"
        f.write_text("def util(): pass\n")
        assert not should_exclude(f, project_root, include_overrides=["vendor/lib"])

    def test_without_override_excluded(self, tmp_path):
        project_root = tmp_path / "project"
        vendor_dir = project_root / "vendor"
        vendor_dir.mkdir(parents=True)
        f = vendor_dir / "util.py"
        f.write_text("def util(): pass\n")
        assert should_exclude(f, project_root)


class TestCollectFiles:
    def test_collect_files_python_app(self, python_app):
        # python_app is inside tests/fixtures which is excluded by default.
        # We use auto_exclude=False to avoid that.
        included, excluded = collect_files(
            python_app,
            auto_exclude=False,
            languages=["python"],
        )
        filenames = {f.name for f in included}
        assert "app.py" in filenames
        assert "models.py" in filenames

    def test_collect_files_respects_language_filter(self, python_app):
        included, _ = collect_files(
            python_app,
            auto_exclude=False,
            languages=["javascript"],
        )
        # No JS files in python_app
        assert len(included) == 0

    def test_collect_files_excludes_node_modules(self, tmp_path):
        # Create a structure with node_modules
        src = tmp_path / "src"
        src.mkdir()
        (src / "app.js").write_text("console.log('hello');")
        nm = tmp_path / "node_modules" / "pkg"
        nm.mkdir(parents=True)
        (nm / "index.js").write_text("module.exports = {};")

        included, excluded = collect_files(tmp_path, auto_exclude=True)
        included_names = {f.name for f in included}
        assert "app.js" in included_names
        assert "index.js" not in included_names
